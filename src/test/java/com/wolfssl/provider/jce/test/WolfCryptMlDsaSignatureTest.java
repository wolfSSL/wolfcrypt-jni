    /* NIST ACVP Vectors from:
     *
     * https://github.com/usnistgov/ACVP-Server/tree/master/gen-val/
     *     json-files/ML-DSA-sigVer-FIPS204
     *
     * Empty-context test cases (one per parameter set) from the pure/external
     * groups. These should exercise the full JCA Signature pipeline,
     * KeyFactory.generatePublic() from raw NIST pubkey wrapped in SPKI, then
     * Signature.initVerify/update/verify() against NIST-published vectors.
     *
     * Only ML-DSA-65 has an empty-context positive test in ACVP. The other two
     * param sets exercise the negative path (testPassed false). */

    /* Convert a hex string to a byte array, short local alias of
     * Util.h2b() to keep the KAT vector declarations readable. */
    private static byte[] hex(String s) {
        return Util.h2b(s);
    }

    /* Build a wolfJCE PublicKey from a raw ML-DSA public key by routing
     * through the JNI layer (importPublicKey raw, exportPublicKeyDer to get
     * X.509 SPKI), then KeyFactory.generatePublic(). */
    private static PublicKey jcePubFromRaw(int level, byte[] rawPub)
        throws Exception {

        MlDsa k = new MlDsa(level);

        try {
            k.importPublicKey(rawPub);
            byte[] spki = k.exportPublicKeyDer(true);
            KeyFactory kf = KeyFactory.getInstance("ML-DSA", "wolfJCE");
            return kf.generatePublic(new X509EncodedKeySpec(spki));
        }
        finally {
            k.releaseNativeStruct();
        }
    }

    private static void assertNistJceVerify(String alg, int level,
        byte[] rawPub, byte[] msg, byte[] sig, boolean expected)
        throws Exception {

        PublicKey pub = jcePubFromRaw(level, rawPub);
        Signature v = Signature.getInstance(alg, "wolfJCE");
        v.initVerify(pub);
        v.update(msg);
        assertEquals("NIST " + alg + " verify", expected, v.verify(sig));
    }

