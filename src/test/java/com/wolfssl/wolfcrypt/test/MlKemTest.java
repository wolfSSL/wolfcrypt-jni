    @Test
    public void testGetLevel() {
        assumeEnabled();

        for (int level : LEVELS) {
            MlKem key = new MlKem(level);
            assertEquals(level, key.getLevel());
            key.releaseNativeStruct();
        }
    }

    @Test
    public void testSizes() {
        assumeEnabled();

        for (int level : LEVELS) {
            MlKem key = new MlKem(level);
            assertEquals(expectedPublicSize(level), key.publicKeySize());
            assertEquals(expectedPrivateSize(level), key.privateKeySize());
            assertEquals(expectedCiphertextSize(level), key.ciphertextSize());
            assertEquals(MlKem.ML_KEM_SHARED_SECRET_SIZE,
                key.sharedSecretSize());
            key.releaseNativeStruct();
        }
    }

    @Test
    public void testEncapsulateDecapsulateSameObject() {
        assumeEnabled();

        Rng rng = new Rng();
        rng.init();

        for (int level : LEVELS) {
            MlKem key = new MlKem(level);
            key.makeKey(rng);

            byte[][] result = key.encapsulate(rng);
            byte[] ciphertext = result[0];
            byte[] secret1 = result[1];

            assertEquals(expectedCiphertextSize(level), ciphertext.length);
            assertEquals(MlKem.ML_KEM_SHARED_SECRET_SIZE, secret1.length);

            byte[] secret2 = key.decapsulate(ciphertext);
            assertArrayEquals(secret1, secret2);

            key.releaseNativeStruct();
        }

        rng.releaseNativeStruct();
    }

    @Test
    public void testEncapsulateDecapsulateCrossObject() {
        assumeEnabled();

        Rng rng = new Rng();
        rng.init();

        for (int level : LEVELS) {
            /* Generate key pair, export raw public and private. */
            MlKem owner = new MlKem(level);
            owner.makeKey(rng);
            byte[] pub = owner.exportPublic();
            byte[] priv = owner.exportPrivate();
            owner.releaseNativeStruct();

            /* Encapsulator only has the public key. */
            MlKem enc = new MlKem(level);
            enc.importPublic(pub);
            byte[][] result = enc.encapsulate(rng);
            byte[] ciphertext = result[0];
            byte[] secretEnc = result[1];
            enc.releaseNativeStruct();

            /* Decapsulator only has the expanded private key. */
            MlKem dec = new MlKem(level);
            dec.importPrivate(priv);
            byte[] secretDec = dec.decapsulate(ciphertext);
            dec.releaseNativeStruct();

            assertArrayEquals(secretEnc, secretDec);
        }

        rng.releaseNativeStruct();
    }

    @Test
    public void testMakeKeyFromSeedDeterministic() {
        assumeEnabled();

        byte[] seed = new byte[MlKem.ML_KEM_SEED_SIZE];
        for (int i = 0; i < seed.length; i++) {
            seed[i] = (byte)(i & 0xFF);
        }

        for (int level : LEVELS) {
            MlKem a = new MlKem(level);
            MlKem b = new MlKem(level);
            a.makeKeyFromSeed(seed);
            b.makeKeyFromSeed(seed);

            assertArrayEquals(a.exportPublic(), b.exportPublic());
            assertArrayEquals(a.exportPrivate(), b.exportPrivate());

            a.releaseNativeStruct();
            b.releaseNativeStruct();
        }
    }

    @Test
    public void testImportExportRoundTrip() {
        assumeEnabled();

        Rng rng = new Rng();
        rng.init();

        for (int level : LEVELS) {
            MlKem key = new MlKem(level);
            key.makeKey(rng);
            byte[] pub = key.exportPublic();
            byte[] priv = key.exportPrivate();
            key.releaseNativeStruct();

            MlKem pubKey = new MlKem(level);
            pubKey.importPublic(pub);
            assertArrayEquals(pub, pubKey.exportPublic());
            pubKey.releaseNativeStruct();

            MlKem privKey = new MlKem(level);
            privKey.importPrivate(priv);
            assertArrayEquals(priv, privKey.exportPrivate());
            privKey.releaseNativeStruct();
        }

        rng.releaseNativeStruct();
    }

