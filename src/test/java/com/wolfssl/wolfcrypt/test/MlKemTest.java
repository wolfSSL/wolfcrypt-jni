    private void keyGenKat(int level, byte[] seed, byte[] ek, byte[] dk) {
        MlKem key = new MlKem(level);
        try {
            key.makeKeyFromSeed(seed);
            assertArrayEquals("KAT public key, level=" + level,
                ek, key.exportPublic());
            assertArrayEquals("KAT private key, level=" + level,
                dk, key.exportPrivate());
        } finally {
            key.releaseNativeStruct();
        }
    }

    @Test
    public void testKeyGenKnownAnswer() {
        assumeEnabled();

        keyGenKat(MlKem.ML_KEM_512, KG_SEED_512, KG_EK_512, KG_DK_512);
        keyGenKat(MlKem.ML_KEM_768, KG_SEED_768, KG_EK_768, KG_DK_768);
        keyGenKat(MlKem.ML_KEM_1024, KG_SEED_1024, KG_EK_1024, KG_DK_1024);
    }

    private void encapsulateKat(int level, byte[] ek, byte[] rand, byte[] ct,
        byte[] ss) {

        MlKem key = new MlKem(level);
        try {
            key.importPublic(ek);
            byte[][] result = key.encapsulateWithRandom(rand);
            assertArrayEquals("KAT ciphertext, level=" + level, ct, result[0]);
            assertArrayEquals("KAT shared secret, level=" + level, ss,
                result[1]);
        } finally {
            key.releaseNativeStruct();
        }
    }

    @Test
    public void testEncapsulateKnownAnswer() {
        assumeEnabled();

        encapsulateKat(MlKem.ML_KEM_512, EN_EK_512, EN_RAND_512, EN_CT_512,
            EN_SS_512);
        encapsulateKat(MlKem.ML_KEM_768, EN_EK_768, EN_RAND_768, EN_CT_768,
            EN_SS_768);
        encapsulateKat(MlKem.ML_KEM_1024, EN_EK_1024, EN_RAND_1024, EN_CT_1024,
            EN_SS_1024);
    }

    private void decapsulateKat(int level, byte[] dk, byte[] ct, byte[] ss) {

        MlKem key = new MlKem(level);
        try {
            key.importPrivate(dk);
            assertArrayEquals("KAT decapsulated secret, level=" + level,
                ss, key.decapsulate(ct));
        } finally {
            key.releaseNativeStruct();
        }
    }

